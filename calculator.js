var num1 = 5;
var num2 = 23;
// Addition
console.log("\n", "Addition of", +num1, "&", +num2, "gives", +(num1 + num2), "\n");
// Subtraction
console.log(" Subtraction of", +num1, "&", +num2, "gives", (+num1 - num2), "\n");
// Multiplication 
console.log(" Multiplication of", +num1, "&", +num2, "gives", +(num1 * num2), "\n");
// Division
console.log(" Division of", +num2, "&", +num1, "gives", +(num2 / num1), "\n");
//Exponentiation
console.log(" Exponentiation of", +num1, "&", +num2, "gives", +(Math.pow(num1, num2)), "\n");
//Modulus
console.log(" Modulus of", num2, "&", num1, "gives", +(num2 % num1), "\n");
